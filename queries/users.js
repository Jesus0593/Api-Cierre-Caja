export const QuerysUser = 
{
    getUserToId:
		`SELECT 
			CODUSUARIO,
			USUARIO,
			CODMODULO,
			MODULO,
			CODSUBMODULO,
			SUBMODULO
		FROM 
			ripAppWeb.PERMISOSUSUARIOS
		WHERE
			CODUSUARIO = @CODUSUARIO
			AND MODULOACTIVO = 'T'
			AND SUBMODULOACTIVO ='T'
			`,
	getUserToPass:
	`
		SELECT 
        	US.USUARIO,
        	US.CODUSUARIO
		FROM 
			USUARIOS US WITH(NOLOCK) 
			INNER JOIN RIP_PERMISOSUSUARIOS PU WITH(NOLOCK) ON PU.CODUSUARIO = US.CODUSUARIO
		WHERE 
			US.NEWPASS = @PASS
	`
		
}
