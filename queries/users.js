export const QuerysUser = 
{
    getUserToId:
		`SELECT 
			CODUSUARIO,
			USUARIO,
			CODMODULO,
			MODULO,
			CODSUBMODULO,
			SUBMODULO
		FROM 
			ripAppWeb.PERMISOSUSUARIOS
		WHERE
			CODUSUARIO = @CODUSUARIO
			AND MODULOACTIVO = 'T'
			AND SUBMODULOACTIVO ='T'
			`,
	getUserToPass:
	`
		SELECT DISTINCT 
			US.CODUSUARIO
			,US.USUARIO
		FROM 
			USUARIOS US 
			INNER JOIN ripAppWeb.PERMISOSUSUARIOS PMU ON PMU.CODUSUARIO = US.CODUSUARIO
		WHERE
			 US.NEWPASS = @PASS
				`
		
}
